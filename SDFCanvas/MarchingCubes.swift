import Foundation
import simd

/// CPU-based Marching Cubes implementation for extracting triangle meshes from SDF trees.
enum MarchingCubes {

    struct ExportMesh {
        var positions: [SIMD3<Float>]
        var normals: [SIMD3<Float>]
        var indices: [UInt32]
    }

    /// Extract a triangle mesh from an SDF tree using Marching Cubes.
    ///
    /// - Parameters:
    ///   - tree: The SDF node tree to evaluate.
    ///   - gridSize: Number of samples along each axis (e.g. 128 = 128^3 voxels).
    ///   - padding: Extra padding around the bounding box (world units).
    /// - Returns: An `ExportMesh` with positions, normals, and triangle indices.
    static func extract(from tree: SDFNode, gridSize: Int, padding: Float = 0.2) -> ExportMesh {
        let (bbMin, bbMax) = tree.boundingBox()
        let lo = bbMin - SIMD3(repeating: padding)
        let hi = bbMax + SIMD3(repeating: padding)
        let extent = hi - lo
        let step = extent / Float(gridSize)

        let nx = gridSize + 1
        let ny = gridSize + 1
        let nz = gridSize + 1

        // Sample the SDF at all grid vertices
        var values = [Float](repeating: 0, count: nx * ny * nz)
        for iz in 0..<nz {
            for iy in 0..<ny {
                for ix in 0..<nx {
                    let p = lo + SIMD3(Float(ix), Float(iy), Float(iz)) * step
                    values[ix + iy * nx + iz * nx * ny] = tree.evaluate(at: p)
                }
            }
        }

        var positions: [SIMD3<Float>] = []
        var normals: [SIMD3<Float>] = []
        var indices: [UInt32] = []

        // Vertex deduplication via spatial hashing
        var vertexMap: [SIMD3<Int32>: UInt32] = [:]
        let quantScale: Float = 1000.0

        func addVertex(_ pos: SIMD3<Float>) -> UInt32 {
            let key = SIMD3<Int32>(
                Int32(pos.x * quantScale),
                Int32(pos.y * quantScale),
                Int32(pos.z * quantScale)
            )
            if let existing = vertexMap[key] {
                return existing
            }
            let idx = UInt32(positions.count)
            positions.append(pos)
            normals.append(tree.normal(at: pos))
            vertexMap[key] = idx
            return idx
        }

        // Process each voxel cube
        for iz in 0..<gridSize {
            for iy in 0..<gridSize {
                for ix in 0..<gridSize {
                    let v = cornerValues(ix: ix, iy: iy, iz: iz, nx: nx, ny: ny, values: values)
                    let cubeIndex = cubeIndex(from: v)

                    guard edgeTable[cubeIndex] != 0 else { continue }

                    let corners = cornerPositions(ix: ix, iy: iy, iz: iz, lo: lo, step: step)

                    // Interpolate edge vertices
                    var edgeVerts = [SIMD3<Float>](repeating: .zero, count: 12)
                    let et = edgeTable[cubeIndex]
                    if et & 1    != 0 { edgeVerts[0]  = interpolate(corners[0], corners[1], v[0], v[1]) }
                    if et & 2    != 0 { edgeVerts[1]  = interpolate(corners[1], corners[2], v[1], v[2]) }
                    if et & 4    != 0 { edgeVerts[2]  = interpolate(corners[2], corners[3], v[2], v[3]) }
                    if et & 8    != 0 { edgeVerts[3]  = interpolate(corners[3], corners[0], v[3], v[0]) }
                    if et & 16   != 0 { edgeVerts[4]  = interpolate(corners[4], corners[5], v[4], v[5]) }
                    if et & 32   != 0 { edgeVerts[5]  = interpolate(corners[5], corners[6], v[5], v[6]) }
                    if et & 64   != 0 { edgeVerts[6]  = interpolate(corners[6], corners[7], v[6], v[7]) }
                    if et & 128  != 0 { edgeVerts[7]  = interpolate(corners[7], corners[4], v[7], v[4]) }
                    if et & 256  != 0 { edgeVerts[8]  = interpolate(corners[0], corners[4], v[0], v[4]) }
                    if et & 512  != 0 { edgeVerts[9]  = interpolate(corners[1], corners[5], v[1], v[5]) }
                    if et & 1024 != 0 { edgeVerts[10] = interpolate(corners[2], corners[6], v[2], v[6]) }
                    if et & 2048 != 0 { edgeVerts[11] = interpolate(corners[3], corners[7], v[3], v[7]) }

                    // Emit triangles
                    let tri = triTable[cubeIndex]
                    var i = 0
                    while i < tri.count && tri[i] != -1 {
                        let i0 = addVertex(edgeVerts[tri[i]])
                        let i1 = addVertex(edgeVerts[tri[i + 1]])
                        let i2 = addVertex(edgeVerts[tri[i + 2]])
                        indices.append(contentsOf: [i0, i1, i2])
                        i += 3
                    }
                }
            }
        }

        return ExportMesh(positions: positions, normals: normals, indices: indices)
    }

    // MARK: - Helpers

    private static func cornerValues(
        ix: Int, iy: Int, iz: Int,
        nx: Int, ny: Int, values: [Float]
    ) -> [Float] {
        let idx = { (x: Int, y: Int, z: Int) -> Int in x + y * nx + z * nx * ny }
        return [
            values[idx(ix, iy, iz)],
            values[idx(ix + 1, iy, iz)],
            values[idx(ix + 1, iy + 1, iz)],
            values[idx(ix, iy + 1, iz)],
            values[idx(ix, iy, iz + 1)],
            values[idx(ix + 1, iy, iz + 1)],
            values[idx(ix + 1, iy + 1, iz + 1)],
            values[idx(ix, iy + 1, iz + 1)],
        ]
    }

    private static func cornerPositions(
        ix: Int, iy: Int, iz: Int,
        lo: SIMD3<Float>, step: SIMD3<Float>
    ) -> [SIMD3<Float>] {
        let base = lo + SIMD3(Float(ix), Float(iy), Float(iz)) * step
        return [
            base,
            base + SIMD3(step.x, 0, 0),
            base + SIMD3(step.x, step.y, 0),
            base + SIMD3(0, step.y, 0),
            base + SIMD3(0, 0, step.z),
            base + SIMD3(step.x, 0, step.z),
            base + step,
            base + SIMD3(0, step.y, step.z),
        ]
    }

    private static func cubeIndex(from v: [Float]) -> Int {
        var idx = 0
        for i in 0..<8 {
            if v[i] < 0 { idx |= (1 << i) }
        }
        return idx
    }

    private static func interpolate(
        _ p1: SIMD3<Float>, _ p2: SIMD3<Float>,
        _ v1: Float, _ v2: Float
    ) -> SIMD3<Float> {
        if Swift.abs(v1) < 0.00001 { return p1 }
        if Swift.abs(v2) < 0.00001 { return p2 }
        if Swift.abs(v1 - v2) < 0.00001 { return p1 }
        let t = -v1 / (v2 - v1)
        return p1 + t * (p2 - p1)
    }

